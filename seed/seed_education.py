async def main():
    async with async_session_factory() as session:
        total = 0
        for slug, keywords in SEGMENTS.items():
            seg = (await session.execute(
                select(Segment).where(Segment.slug == slug)
            )).scalar_one()

            deleted = (await session.execute(
                delete(SegmentKeyword).where(SegmentKeyword.segment_id == seg.id)
            )).rowcount

            count = 0
            for kw_type in ["demand", "stop", "synonym"]:
                for text in keywords[kw_type]:
                    session.add(SegmentKeyword(
                        segment_id=seg.id,
                        text=text,
                        keyword_type=kw_type,
                        is_active=True,
                    ))
                    count += 1

            await session.commit()
            total += count
            print(f"  {seg.title_ru}: {deleted}→{count} ({len(keywords['demand'])}d/{len(keywords['stop'])}s/{len(keywords['synonym'])}y)")

        print(f"Total: {total} keywords across 4 segments")


if __name__ == "__main__":
    asyncio.run(main())
