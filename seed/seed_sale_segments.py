async def main():
    async with async_session_factory() as session:
        for slug, keywords in SEGMENTS.items():
            seg = (await session.execute(
                select(Segment).where(Segment.slug == slug)
            )).scalar_one()
            print(f"Segment: {seg.title_ru} (id={seg.id})")

            await session.execute(
                delete(SegmentKeyword).where(SegmentKeyword.segment_id == seg.id)
            )

            count = 0
            for kw_type in ["demand", "stop", "synonym"]:
                for text in keywords[kw_type]:
                    session.add(SegmentKeyword(
                        segment_id=seg.id,
                        text=text,
                        keyword_type=kw_type,
                        is_active=True,
                    ))
                    count += 1

            await session.commit()
            print(f"  {count} keywords: {len(keywords['demand'])} demand, {len(keywords['stop'])} stop, {len(keywords['synonym'])} synonym")


if __name__ == "__main__":
    asyncio.run(main())
